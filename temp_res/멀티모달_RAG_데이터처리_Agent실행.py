def pair_image_and_text(image_path, text_path):

    # 디렉토리가 존재하지 않을 경우를 대비해 빈 리스트로 초기화
    try:
        images = os.listdir(image_path)
    except FileNotFoundError:
        images = []

    try:
        texts = os.listdir(text_path)
    except FileNotFoundError:
        texts = []

    # 확장자를 제외한 파일명(base name)을 키(key)로, 원본 파일명을 값(value)으로 하는 딕셔너리 생성
    img_dict = {os.path.splitext(img)[0]: os.path.join(image_path, img) for img in images if img.endswith('.png')}
    txt_dict = {os.path.splitext(txt)[0]: os.path.join(text_path, txt) for txt in texts if txt.endswith('.txt')}

    # 두 딕셔너리의 키(base name)를 합쳐서 중복 없는 모든 파일명 <집합>을 생성
    all_bases = set(img_dict.keys()).union(set(txt_dict.keys()))

    result = []
    # 파일명 기준으로 정렬하여 순차적으로 처리
    for base in sorted(all_bases):
        # 딕셔너리에 해당 키가 있으면 파일명을, 없으면 지정된 문자열을 반환
        img_item = img_dict.get(base, 'no png')
        txt_item = txt_dict.get(base, 'no txt')

        result.append((img_item, txt_item))

    print(img_dict)

    return result

